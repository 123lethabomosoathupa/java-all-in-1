package forevencounter;

/**
 *
 * @author letha
 */
public class ForEvenCounter {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        for (int number = 2; number <= 20; number += 2) {
            System.out.print(number + " ");
        }
        System.out.println();
    }

}
